#include<iostream>

using namespace std;

int main() {
    int a = 45, b = 20;

    cout << "\n ------------------------------------------\n";
    cout << "\n -       Basic Arithmetic Operation       -\n" << endl;
    cout << " -             " << a << " + " << b << " = " << a + b << "               -" << endl;
    cout << " -             " << a << " - " << b << " = " << a - b << "               -" << endl;
    cout << " -             " << a << " * " << b << " = " << a * b << "              -" << endl;
    cout << " -             " << a << " / " << b << " = " << a / b << "                -" << endl;
    cout << "\n ------------------------------------------\n" << endl;
    return 0;
}
