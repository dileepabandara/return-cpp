#include<iostream>

using namespace std;

int main() {
    int num1 = 100;
    int num2 = 25;

    int add = num1 + num2;
    int sub = num1 - num2;
    int mul = num1 * num2;
    int div = num1 / num2;

    cout << "If we use 100 and 25 then\n" << endl;
    cout << "addition = " << add << endl;
    cout << "subtraction = " << sub << endl;
    cout << "multiplication = " << mul << endl;
    cout << "division = " << div << endl;
    return 0;
}
